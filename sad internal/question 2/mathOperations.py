def addition(a,b):
    return a+b
def subtraction(a,b):
    return a-b
def multiplication(a,b):
    return a*b
def division(a,b):
    return a/b
def modulus(a,b):
    return a%b

n = int(input("enter a number:"))
m = int(input("enter second number:"))

print(addition(n, m))
print(subtraction(n, m))
print(multiplication(n, m))
